package random1;

// basic while loop example to print the numbers.
public class WhileLoopExample {
    public static void main(String[] args) {
        WhileLoopExample sc = new WhileLoopExample();
        sc.exam();

    }

    public void exam ()
    {
        int i=0;

        while (i<=10)
        {
            System.out.println(" " +i);
            i++;
        }
    }
}
